# string slicing
b='shoaib is a programmer and ds student'
print(len(b))
print(b[::2])
print(b[::])
print(b[-1:-5:-2])
print(b.isalnum())
print(b.isalpha())
print(b.endswith('t'))
print(b.count('s'))
print(b.capitalize())
print(b.replace('programmer','data engineer'))
print(b.lower())
print(b.find('ds'))